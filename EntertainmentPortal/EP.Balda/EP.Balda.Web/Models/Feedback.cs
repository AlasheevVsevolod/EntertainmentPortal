namespace EP.Balda.Web.Models
{
    /// <summary>
    /// Represents the user's feedback.
    /// </summary>
    public class Feedback
    {
        /// <summary>
        /// Name property. Represents player's name.
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Email property. Represents player's e-mail.
        /// </summary>
        public string Email { get; set; }

        /// <summary>
        /// Message property. Represents player's message.
        /// </summary>
        public string Message { get; set; }
    }
}
